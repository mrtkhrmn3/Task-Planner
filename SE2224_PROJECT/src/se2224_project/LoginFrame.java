    public void popUp(){
        
        
        try{
            Connection connection = DriverManager.getConnection(url,username,password);
            
            String query="SELECT * FROM information WHERE deadline=CURDATE()+INTERVAL 1 DAY";
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery(query);
            
            StringBuilder message = new StringBuilder();
            while(resultSet.next()){
                String task = resultSet.getString("taskName");
                message.append(task).append("\n");
               
            }
            
            javax.swing.JOptionPane.showMessageDialog(null,     "Tasks with 1 day left:\n"  + message.toString());
            
            resultSet.close();
            statement.close();
            connection.close();
        }catch(Exception e){
            e.printStackTrace();
        }
    }

