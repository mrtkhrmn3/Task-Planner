    private void JLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JLoginActionPerformed
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/se2224_project2","root","Mert42582001");
            String username = JUsername.getText();
            String passwords = JPassword.getText();
            
            Statement stm = con.createStatement();
            
            String sql = "select * from login where username='"+username+"' and passwords='"+passwords+"'";
            
            ResultSet rs = stm.executeQuery(sql);
            
            if(rs.next()){
                dispose();
                MainFrame mf = new MainFrame();
                mf.show();
                popUp();
            }
            else{
                JOptionPane.showMessageDialog(this, "username or password wrong!");
                JUsername.setText("");
                JPassword.setText("");
            }
            
            con.close();
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_JLoginActionPerformed

